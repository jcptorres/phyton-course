# Metodos de listas

# append

a_list = [1,2,3,4,5]
a_list.append(6)
print(a_list)

# clear

a_list.clear()
print(a_list)

# extend
# El metodo extend() une lista con otra

l1= [1,2,3,4]
l2= [8,4,6,9]
print (l1)

l1.extend(l2)
print (l1)

# Count

a = ['juan','fernando', 'Juan', 'carlos', 'juan']
print (a.count('juan'))

# index

print (a.index('carlos'))


